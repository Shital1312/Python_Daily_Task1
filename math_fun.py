import math as m
# from math import *
x = -10.5
print(m.ceil(x))  # next integer number

print(m.floor(x))  # decreases the number

print(m.sqrt(16))  # return positive square toot

print(m.factorial(5))  # return factorial number
print(m.pi)  # return pi value constant value
print(m.fabs(x))  # return positive value
print(m.fsum([10, x]))  # return sum
print(m.pow(5, 2))  # raise x value to the power of y
print(m.gcd(36, 24))  # gives the greatest common divisor of x and y.
print(m.trunc(x))  # return float integer value with change negative to positive and positive to negative

